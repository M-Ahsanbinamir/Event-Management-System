    private void updatebookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebookActionPerformed
        // TODO add your handling code here:
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("events.csv"))) {
        DefaultTableModel tableModel = (DefaultTableModel) booktable.getModel();
        int rowCount = tableModel.getRowCount();
        int columnCount = tableModel.getColumnCount();

        // Write the header
        for (int column = 0; column < columnCount; column++) {
            writer.write(tableModel.getColumnName(column));
            if (column < columnCount - 1) {
                writer.write(",");
            }
        }
        writer.newLine();

        // Write the data
        for (int row = 0; row < rowCount; row++) {
            for (int column = 0; column < columnCount; column++) {
                writer.write((String) tableModel.getValueAt(row, column));
                if (column < columnCount - 1) {
                    writer.write(",");
                }
            }
            writer.newLine();
        }

        System.out.println("Changes saved successfully!");
    } catch (IOException e) {
        e.printStackTrace();
    }        
    }//GEN-LAST:event_updatebookActionPerformed

